	public function destroy()
	{
		$id = $this->uri->segment(4);

		$data['payment'] = $this->payment_model->getPayment($id);

		if($data['payment'] == "")
		{
			$data['status'] = $this->order_model->deleteOrder($id);

			if($data['status'] == true)
			{
				$data = array(
							'delete_status' => '1',
						);

				$this->session->set_userdata($data);
			}

			redirect('account/order');
		}
		else
		{

			$data = array(
						'delete_status' => '2',
					);

			$this->session->set_userdata($data);
			
			redirect('account/order');
		}
	}
